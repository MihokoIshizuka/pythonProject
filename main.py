print('Hello World!')

# カンマ区切りで半角空いた出力ができる
print('Hello', 'paiza')

#　print 関数を使うとデフォルトで末尾に改行が入るが、end= で文字列を指定することで末尾を変更できる
print('Hello', end=",")
print('World')

print('Hello', end="")
print('World')

# / で割り算
print(1 / 5)
print(1.0 / 5.0)

# //で割り算⇒切り捨てて除算
print(5 // 2)
print(1.7 // 0.6)
print(3.14 // 1)